"""# Tokenization: Splits text into tokens for processing.

"""

from indicnlp.tokenize import indic_tokenize
text = "नमस्ते, यह एक उदाहरण है।"
text2= "Hi there, I am Arghya Nath"
tokens = list(indic_tokenize.trivial_tokenize(text, lang='hi'))
tokens2 = list(indic_tokenize.trivial_tokenize(text2, lang='hi'))
print("Tokens:", tokens)
print("Tokens:",tokens2)

"""# Detokenization"""

detokenized_text = ' '.join(tokens)
print("De-tokenized Text:", detokenized_text)

