"""# Sentence Splitting:dividing a large text document or paragraph into individual sentences."""

from indicnlp.tokenize.sentence_tokenize import sentence_split

# Kannada Text
text = "ಇದು ಒಂದು ಉದಾಹರಣೆಯ ವಾಕ್ಯ. ಇದು ಮತ್ತೊಂದು ವಾಕ್ಯ."
text2= "বাংলা সংস্কৃতি তার বৈচিত্র্য এবং ঐতিহ্যের জন্য বিখ্যাত। এখানে সঙ্গীত, নৃত্য, শিল্প, এবং সাহিত্য গভীরভাবে মানুষের জীবনের সাথে জড়িত। রবীন্দ্রসংগীত ও লোকগীতি মানুষের অনুভূতিকে প্রকাশ করে, আর দুর্গাপূজা, যা বাঙালির প্রধান উৎসব, সমাজে আনন্দ ও ঐক্যের বার্তা নিয়ে আসে। "

# Sentence Splitting
sentences = sentence_split(text, lang='kn')
sentences2 = sentence_split(text2, lang='bn')

print("Original Text:", text)
